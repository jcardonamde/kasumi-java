    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Acción de guardado del cliente
        if (validarCampos()) {
            ClienteModel est = new ClienteModel();
            est.setNombre(txtNombreCliente.getText());
            est.setApellido(txtApellidoCliente.getText());
            est.setEmail(txtEmailCliente.getText());
            est.setTelefono(txtTelefonoCliente.getText());
            boolean guardado = ClienteDao.guardar(est);
            
            if (guardado) {
                limpiarCampos();
                listarDatos();
            } else {
                JOptionPane.showMessageDialog(null, ClienteDao.mensaje);
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Acción para consultar un cliente
        if(!txtIdCliente.getText().isEmpty() && txtIdCliente.getText().matches("\\d+")) {
            int idCliente = Integer.parseInt(txtIdCliente.getText().trim());
            ClienteModel c = ClienteDao.consultar(idCliente);
            
            if(c!=null) {
                txtNombreCliente.setText(c.getNombre());
                txtApellidoCliente.setText(c.getApellido());
                txtEmailCliente.setText(c.getEmail());
                txtTelefonoCliente.setText(c.getTelefono());
            } else {
                JOptionPane.showMessageDialog(null, "No se encontro un cliente con ese Id");
                limpiarCampos();
                txtIdCliente.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Acción para actualizar información de un cliente
        if (validarCampos()) {
            ClienteModel cli = new ClienteModel();
            cli.setIdCliente(Integer.parseInt(txtIdCliente.getText()));
            cli.setNombre(txtNombreCliente.getText());
            cli.setApellido(txtApellidoCliente.getText());
            cli.setEmail(txtEmailCliente.getText());
            cli.setTelefono(txtTelefonoCliente.getText());
            boolean guardado = ClienteDao.actualizar(cli);
            
            if (guardado) {
                limpiarCampos();
                listarDatos();
            } else {
                JOptionPane.showMessageDialog(null, ClienteDao.mensaje);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Acción para eliminar un cliente de la BD
        if(!txtIdCliente.getText().isEmpty() && txtIdCliente.getText().matches("\\d+")) {
            int codigo = Integer.parseInt(txtIdCliente.getText().trim());
            boolean eliminado = ClienteDao.eliminar(codigo);
            
            if(eliminado) {
                JOptionPane.showMessageDialog(null, ClienteDao.mensaje);
                limpiarCampos();
                listarDatos();
                txtIdCliente.requestFocus();
            }
            JOptionPane.showMessageDialog(null, ClienteDao.mensaje);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

