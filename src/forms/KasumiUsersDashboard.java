    private boolean validarCampos() {
        if(txtNombreCliente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el nombre");
            txtNombreCliente.requestFocus();
            return false;
        } else if (txtApellidoCliente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el apellido");
            txtApellidoCliente.requestFocus();
            return false;
        } else if (txtEmailCliente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el email");
            txtEmailCliente.requestFocus();
            return false;
        } else if (txtTelefonoCliente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el telefono");
            txtTelefonoCliente.requestFocus();
            return false;
        } else {
            return true;
        }
    }
    
    public void limpiarCampos() {
        txtIdCliente.setText("");
        txtNombreCliente.setText("");
        txtApellidoCliente.setText("");
        txtEmailCliente.setText("");
        txtTelefonoCliente.setText("");
        txtNombreCliente.requestFocus();
    }
    
    private void listarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaClientes.getModel();
        modelo.setRowCount(0);
        modelo.setColumnCount(0);
        ResultSet resultado = ClienteDao.listar();
        try {
            ResultSetMetaData metaData = resultado.getMetaData();
            int columnas = metaData.getColumnCount();
            
            for (int i = 1; i < columnas; i++) {
                modelo.addColumn(metaData.getColumnName(i).toUpperCase());
            }
            
            while (resultado.next()) {
                Object[] fila = new Object[columnas];
          
                for (int i = 1; i < columnas; i++) {
                    fila[i-1] = resultado.getObject(i);
                }
                modelo.addRow(fila);
            }
        } catch (SQLException ex) {
            System.getLogger(KasumiUsersDashboard.class.getName()).log(Logger.Level.ERROR, (String) null, ex);
        }
    }
